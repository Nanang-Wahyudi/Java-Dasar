package Iseng_Looping;

public class Loop_1 {

    public static void main(String[] args) {

        // Program Kelipatan 7 menggunakan for

        int nilai;

        System.out.println();
        for (nilai = 14; nilai <= 100; nilai = nilai + 14) {

            System.out.print(nilai + " - ");

        }
        System.out.println();


    }

}

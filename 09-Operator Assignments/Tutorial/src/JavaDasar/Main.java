package JavaDasar;

public class Main {

    public static void main(String[] args) {

        // Operator Assignment

        // Operator Assignment Penjumlahan
        System.out.println();
        int jumlah = 10;
        jumlah += 5;
        System.out.println(" Nilai Jumlah  = " + jumlah);

        // Operator Assignment Pengurangan
        int kurang = 10;
        kurang -= 5;
        System.out.println(" Nilai Kurang  = " + kurang);

        // Operator Assignment Perkalian
        int kali = 10;
        kali *= 5;
        System.out.println(" Nilai Kali    = " + kali);

        // Operator Assignment Pembagian
        int bagi = 10;
        bagi /= 5;
        System.out.println(" Nilai Bagi    = " + bagi);

        // Operator Assignment Modulus
        int modulus = 10;
        modulus %= 5;
        System.out.println(" Nilai Modulus = " + modulus);

    }

}
